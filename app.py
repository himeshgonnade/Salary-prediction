import streamlit as st
import pickle
import pandas as pd

# Load the trained model
@st.cache_resource
def load_model():
    with open('random_forest_regressor_model.pkl', 'rb') as file:
        model = pickle.load(file)
    return model

model = load_model()

st.title('Salary Prediction App')
st.write('Enter the details below to predict the salary.')

# Input fields for features
age = st.slider('Age', 18, 65, 30)
gender = st.selectbox('Gender (0=Female, 1=Male)', options=[0, 1])
education_level = st.selectbox('Education Level (0=Bachelors, 1=High School, 2=Masters, 3=PhD)', options=[0, 1, 2, 3, 4, 5]) # Assuming your original encoding for Education Level
job_title = st.number_input('Job Title (Encoded Integer)', min_value=0, max_value=200, value=0) # Max value depends on number of unique job titles
years_of_experience = st.slider('Years of Experience', 0.0, 40.0, 5.0, step=0.5)

# When the user clicks the 'Predict' button
if st.button('Predict Salary'):
    # Create a DataFrame from the inputs
    input_data = pd.DataFrame([
        {
            'Age': age,
            'Gender': gender,
            'Education Level': education_level,
            'Job Title': job_title,
            'Years of Experience': years_of_experience
        }
    ])

    # Make prediction
    prediction = model.predict(input_data)[0]

    st.success(f'Predicted Salary: ${prediction:,.2f}')